    private void btnSwitchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSwitchActionPerformed
        // TODO add your handling code here:
        
        // jika buku
        if(btnSwitch.isSelected()){
            // add data hide
            addAuthorPanel.setVisible(false);
            addBukuPanel.setVisible(false);
            
            // text button menjadi author
            btnSwitch.setText("Author");
            
            // judul utama menjadi database author
            label_aplikasi.setText("Database Author");
            
            // set true
            isAuthor = true;
            
            // buat panel author
            authorPanel = new AuthorPanel(false);
            
            // dengan grid
            authorPanel.setLayout(new GridBagLayout());
            
            // mengisi panel scrol dengan author panel
            dataScrollPanel.setViewportView(authorPanel);
            
            // dapatkan data
            getDataListAuthorDB();
        }
        
        // jika pilih author
        else {
            
            // hide panel add data
            addAuthorPanel.setVisible(false);
            addBukuPanel.setVisible(false);
            
            // tombol menjadi buku dan
            // judul menjadi database "buku"
            btnSwitch.setText("Buku");
            label_aplikasi.setText("Database Buku");
            
            // author = false
            isAuthor = false;
            
            // tampilkan panel buku
            bukuPanel = new BukuPanel(false);
            
            // dengan grid
            bukuPanel.setLayout(new GridBagLayout());
            
            // mengisi scrol panel kosong dengan panel buku
            dataScrollPanel.setViewportView(bukuPanel);
            
            // dapatkan data list
            getDataListBukuDB();
        }

    }//GEN-LAST:event_btnSwitchActionPerformed

