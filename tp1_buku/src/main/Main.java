    // method mendapatkan data author dari database
    // sekaligus menampilkannya
    private void getDataListAuthorDB(){        
        try {
            // query
            String sql = "select * from author";
            ResultSet res = dbCon.getStatement().executeQuery(sql);  
            
            // get atribut
            while (res.next()) {
                Author dataAuthor = new Author(res.getInt("id_author"), res.getString("nama"), res.getInt("jumlah_buku"), res.getString("image_author"));
                
                // tampilkan data
                viewListPanelAuthor(dataAuthor);
            }
        } catch (Exception e) {
            System.err.println("error! " + e.getMessage());
        }
        
    }
    
    // method mendapatkan data Buku dari database
    // sekaligus menampilkannya
    private void getDataListBukuDB(){
        try {
            // Query
            String sql = "select * from buku";
            ResultSet res = dbCon.getStatement().executeQuery(sql);  
            
            // get atribut
            while (res.next()) {
                Buku dataBuku = new Buku(res.getInt("id_buku"), res.getString("judul"), res.getString("penerbit"), res.getString("author"), res.getString("deskripsi"), res.getString("image_buku"));
                
                // tampilkan panel data
                viewListPanelBuku(dataBuku);
            }
        } catch (Exception e) {
            System.err.println("error! " + e.getMessage());
        }
    }
    
    // menampilkan data author dengan list grid
    private void viewListPanelAuthor(Author dataAuthor)
    {
        // grid
        GridBagConstraints gridBC = new GridBagConstraints();
        
        // sbenyak komponen
        gridBC.gridy = authorPanel.getComponentCount();
        gridBC.gridx = 0;
        
        gridBC.fill = GridBagConstraints.HORIZONTAL;
        gridBC.anchor = GridBagConstraints.PAGE_START;
       
        gridBC.insets = new Insets(5, 2, 5, 2);
        AuthorPanel authorPanelTemp = new AuthorPanel(dataAuthor);

        authorPanel.add(authorPanelTemp, gridBC);
        authorPanel.revalidate();
        authorPanel.invalidate();
        authorPanel.repaint();
        pack();
    }   
    
